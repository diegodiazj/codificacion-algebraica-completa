    public void fondo() {
        int f = 60;
        Graphics g = getGraphics();
        super.paintComponents(g);
        g.setColor(Color.RED);
        g.drawLine(60, 600, 1500, 600);
        g.setColor(Color.BLACK);
        for (int i = 0; i < 16; i++) {
            g.drawLine(f, 450, f, 750);
            f += 90;
        }
    }

