    private void MILLERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MILLERActionPerformed

        Graphics g = getGraphics();
        fondo();
        String tramaF = NumBin.getText() + " ";
        char[] numero = tramaF.toCharArray();
        int estAn = estadoAnterior;
        int x = 60;
        int y = 0;
        g.setColor(Color.BLUE);
        for (int i = 0; i < numero.length-1; i++) {
            int actual = Character.getNumericValue(numero[i]);
            int estaF = Character.getNumericValue(numero[i + 1]);
            if (actual == 1 || actual == 0) {
                if (actual == 1) {
                    if (estAn == 1) {
                        g.drawLine(x, 500, (x + 45), 500);
                        g.drawLine((x + 45), 500, (x + 45), 700);
                        g.drawLine((x + 45), 700, (x + 90), 700);
                        estAn = -1;
                    } else {
                        g.drawLine(x, 700, (x + 45), 700);
                        g.drawLine((x + 45), 700, (x + 45), 500);
                        g.drawLine((x + 45), 500, (x + 90), 500);
                        estAn = 1;
                    }
                } else {
                    if (estaF == 0) {
                        if (estAn == 1) {
                            g.drawLine(x, 500, (x + 90), 500);
                            estAn = -1;
                        } else {
                            g.drawLine(x, 700, (x + 90), 700);
                            estAn = +1;
                        }
                        g.drawLine((x + 90), 500, (x + 90), 700);
                    } else {
                        if (estAn == 1) {
                            g.drawLine(x, 500, (x + 90), 500);
                        } else {
                            g.drawLine(x, 700, (x + 90), 700);

                        }
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "No es un número valido");
                fondo();
                i = numero.length;
            }
            x += 90;
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_MILLERActionPerformed

    private void CMIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CMIActionPerformed

        Graphics g = getGraphics();
        fondo();
        char[] numero = NumBin.getText().toCharArray();
        int estAn = estadoAnterior;
        int x = 60;
        g.setColor(Color.BLUE);
        for (int i = 0; i < numero.length; i++) {
            int actual = Character.getNumericValue(numero[i]);

            if (actual == 1 || actual == 0) {
                if (actual == 0) {
                    g.drawLine(x, 700, (x + 45), 700);
                    g.drawLine((x + 45), 700, (x + 45), 500);
                    g.drawLine((x + 45), 500, (x + 90), 500);
                } else if (actual == 1 && estAn == 1) {
                    g.drawLine(x, 700, (x + 90), 700);
                    estAn = -1;
                } else if (actual == 1 && estAn == -1) {
                    g.drawLine(x, 500, (x + 90), 500);
                    estAn = 1;
                }
            } else {
                JOptionPane.showMessageDialog(this, "No es un número valido");
                fondo();
                i = numero.length;
            }
            x += 90;
        }
    }//GEN-LAST:event_CMIActionPerformed

