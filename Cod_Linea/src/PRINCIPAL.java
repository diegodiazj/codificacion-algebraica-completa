    private void MLT3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MLT3ActionPerformed

        Graphics g = getGraphics();
        fondo();
        char[] numero = NumBin.getText().toCharArray();
        int estAn = estadoAnterior;
        int esta = 0;
        int x = 60;
        int y = 600;

        g.setColor(Color.BLUE);

        for (int i = 0; i < numero.length; i++) {
            int actual = Character.getNumericValue(numero[i]);
            if (actual == 1 || actual == 0) {
                if (actual == 0) {
                    g.drawLine(x, y, (x + 90), y);
                    x += 90;
                } else {
                    if (estAn == 1 && esta == 0) {
                        g.drawLine(x, y, x, (y + 100));
                        g.drawLine(x, (y + 100), (x + 90), (y + 100));
                        y += 100;
                        x += 90;
                        estAn = 0;
                        esta = -1;
                    } else if (estAn == 0 && esta == -1) {
                        g.drawLine(x, y, x, (y - 100));
                        g.drawLine(x, (y - 100), (x + 90), (y - 100));
                        y -= 100;
                        x += 90;
                        estAn = -1;
                        esta = 0;
                    } else if (esta == 0 && estAn == -1) {
                        g.drawLine(x, y, x, (y - 100));
                        g.drawLine(x, (y - 100), (x + 90), (y - 100));
                        y -= 100;
                        x += 90;
                        estAn = 0;
                        esta = 1;
                    } else if (esta == 1 && estAn == 0) {
                        g.drawLine(x, y, x, (y + 100));
                        g.drawLine(x, (y + 100), (x + 90), (y + 100));
                        y += 100;
                        x += 90;
                        estAn = 1;
                        esta = 0;
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "No es un número valido");
                fondo();
                i = numero.length;
            }

        }
    }//GEN-LAST:event_MLT3ActionPerformed


